"""Structural skeleton tests for examples/trace-jepa.

Mirrors tests/test_ijepa_cifar10_skeleton.py: validates the example's
file shape and key contract elements without actually running training
(which needs a GPU + the synthetic corpus generated by prepare.py).
Catches regressions like:

- accidental mutation of frozen prepare.py (loss of WebDataset shard
  schema, removal of canary.json output, removal of manifest.json)
- removal of required emit_progress / assert_no_grad_on_target calls
  from train.py (these would normally be enforced by the AST validator
  during a campaign — this test catches them at PR time)
- drift between program.md hard-rules wording and the JEPA_HARD_RULES
  prompt fragment
- config.yaml losing the Basilica target / probe_auroc objective /
  recalibrated forecaster defaults / Phase-3 codebook search axes /
  Phase-3 30-iter budget

The actual end-to-end smoke (`./run.sh smoke`) and the Basilica
campaign run are tracked in `TODO.md` Phase-3 — they require runtime
resources outside CI.
"""

from __future__ import annotations

from pathlib import Path

import yaml

EXAMPLE_DIR = Path(__file__).parent.parent / "examples" / "trace-jepa"



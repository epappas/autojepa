class TestConfigYaml:
    def _config(self) -> dict:
        return yaml.safe_load((EXAMPLE_DIR / "config.yaml").read_text())

    def test_objective_is_probe_auroc_max(self) -> None:
        cfg = self._config()
        assert cfg["objective"]["metric"] == "probe_auroc"
        assert cfg["objective"]["direction"] == "max"

    def test_target_is_basilica(self) -> None:
        cfg = self._config()
        assert cfg["target"]["type"] == "basilica"

    def test_basilica_accepts_a100(self) -> None:
        cfg = self._config()
        assert "A100" in cfg["target"]["basilica"]["gpu_models"]

    def test_basilica_gpu_count_one(self) -> None:
        """Phase-3 single-GPU spec per writeup §11."""
        cfg = self._config()
        assert cfg["target"]["basilica"]["gpu_count"] == 1

    def test_policy_is_hybrid_with_jepa_widened_defaults(self) -> None:
        cfg = self._config()
        assert cfg["policy"]["type"] == "hybrid"
        assert cfg["policy"]["hybrid_param_explore_iters"] >= 25
        assert cfg["policy"]["hybrid_stall_threshold"] >= 5

    def test_intra_iteration_cancel_uses_ssl_defaults(self) -> None:
        cfg = self._config()
        ic = cfg["controller"]["intra_iteration_cancel"]
        assert ic["enabled"] is True
        assert ic["min_steps"] >= 2000
        assert ic["poll_interval_s"] >= 30.0
        assert ic["min_reports_before_decide"] >= 10

    def test_max_iterations_is_phase3_budget(self) -> None:
        """Writeup §11 Phase-3 calls for 30 iters > Phase-2's 20."""
        cfg = self._config()
        assert cfg["controller"]["max_iterations"] == 30

    def test_codebook_size_search_dim(self) -> None:
        """Phase-3 search axis per docs/research/mts-jepa.md and
        TODO.md Phase-3 codebook bottleneck section."""
        cfg = self._config()
        params = cfg["policy"]["params"]
        assert "codebook_size" in params
        assert 0 in params["codebook_size"]  # vanilla JEPA control
        for size in (64, 256, 1024):
            assert size in params["codebook_size"], (
                f"codebook_size={size} required by TODO.md Phase-3"
            )

    def test_codebook_loss_weight_search_dim(self) -> None:
        cfg = self._config()
        params = cfg["policy"]["params"]
        assert "codebook_loss_weight" in params
        for w in (0.0, 0.1, 0.5, 1.0):
            assert w in params["codebook_loss_weight"], (
                f"codebook_loss_weight={w} required by TODO.md Phase-3"
            )

    def test_future_block_mask_weight_in_search(self) -> None:
        cfg = self._config()
        params = cfg["policy"]["params"]
        assert "future_block_weight" in params
        assert "multi_block_weight" in params

    def test_standard_jepa_search_dims_present(self) -> None:
        cfg = self._config()
        params = cfg["policy"]["params"]
        for dim in (
            "learning_rate",
            "weight_decay",
            "batch_size",
            "max_steps",
            "predictor_depth",
            "num_targets",
            "ema_decay_start",
        ):
            assert dim in params, f"missing standard JEPA search dim: {dim}"
